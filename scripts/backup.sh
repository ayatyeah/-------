#!/bin/sh
# Одна резервная копия данных сайта + удаление старых.
#
# Данные — это один файл store.json: заявки, каталог, новости, настройки.
# Сервер пишет его атомарно (сначала во временный файл, потом переименовывает),
# поэтому копировать можно на ходу: нам либо достанется целый новый файл, либо
# целый предыдущий. Останавливать сайт ради бэкапа не нужно.
#
# Запуск вручную:
#     docker compose exec backup sh /scripts/backup.sh
set -eu

SRC="${STORE_PATH:-/data/store.json}"
DEST="${BACKUP_DIR:-/backups}"
KEEP="${BACKUP_KEEP_DAYS:-14}"

log() { echo "[$(date '+%Y-%m-%d %H:%M:%S')] $*"; }

if [ ! -f "$SRC" ]; then
  # Код 3 — «данных пока нет». Отдельный код нужен, чтобы цикл отличил это
  # от настоящей ошибки и повторил попытку скоро, а не через сутки: иначе
  # при первом развёртывании сайт остался бы без копии на целый день.
  log "данных ещё нет ($SRC) — сайт, похоже, только запускается"
  exit 3
fi

mkdir -p "$DEST"

STAMP=$(date '+%Y-%m-%d_%H-%M')
FILE="$DEST/store-$STAMP.json.gz"

# Пишем во временный файл и только потом переименовываем: если процесс
# прервётся посередине, в папке не останется обрезанной копии, которую
# легко принять за целую.
TMP="$FILE.part"
gzip -c "$SRC" > "$TMP"
mv "$TMP" "$FILE"

SIZE=$(wc -c < "$FILE" | tr -d ' ')
log "копия готова: $(basename "$FILE") ($SIZE байт)"

# Проверяем, что архив читается. Битый бэкап хуже отсутствующего: на него
# рассчитывают, а в нужный момент он не открывается.
if ! gzip -t "$FILE" 2>/dev/null; then
  log "ВНИМАНИЕ: архив $(basename "$FILE") не проходит проверку, удаляю"
  rm -f "$FILE"
  exit 1
fi

# Чистим старые копии.
# Имена переменных только латиницей: оболочка в Alpine (ash) кириллицу
# в именах не принимает и падает с «not found».
removed=$(find "$DEST" -name 'store-*.json.gz' -type f -mtime "+$KEEP" -print -delete | wc -l | tr -d ' ')
if [ "$removed" -gt 0 ]; then
  log "удалено старых копий: $removed (храним $KEEP дней)"
fi

total=$(find "$DEST" -name 'store-*.json.gz' -type f | wc -l | tr -d ' ')
log "всего копий в $DEST: $total"
