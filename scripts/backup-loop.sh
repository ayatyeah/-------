#!/bin/sh
# Ежесуточный запуск резервного копирования.
#
# Нарочно простой цикл вместо cron: в контейнере cron требует отдельной
# возни с логами и переменными окружения, а здесь всё видно обычной
# командой docker compose logs backup.
#
# Первая копия делается сразу при старте — так сразу видно, что бэкапы
# работают, и не нужно ждать сутки, чтобы это проверить.
set -eu

EVERY="${BACKUP_EVERY:-86400}"

echo "[бэкап] запущен, период ${EVERY} с, хранение ${BACKUP_KEEP_DAYS:-14} дней"

while true; do
  # Сбой одной копии не должен убивать цикл: диск или права могут подвести
  # разово, а в следующий раз всё получится. Поэтому ошибку показываем,
  # но идём дальше.
  # Имена переменных только латиницей: оболочка в Alpine (ash) кириллицу
  # в именах не принимает и падает с «not found».
  code=0
  sh /scripts/backup.sh || code=$?

  if [ "$code" -eq 3 ]; then
    # Данных ещё нет — сайт не успел подняться. Ждать сутки в этом случае
    # нельзя: развёрнутый сервер остался бы без копии на целый день.
    echo "[бэкап] повторю через минуту"
    sleep 60
    continue
  fi

  [ "$code" -eq 0 ] || echo "[бэкап] копия не удалась (код $code), попробую в следующий раз"
  sleep "$EVERY"
done
